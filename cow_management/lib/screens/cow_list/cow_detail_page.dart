  Future<bool> deleteCow(BuildContext context, String cowId) async {
    final dio = Dio();
    final String? apiUrl = dotenv.env['API_BASE_URL'];
    final token = await Provider.of<UserProvider>(context, listen: false)
        .loadTokenFromStorage();

    if (apiUrl == null || token == null) {
      CowDetailPage._logger.severe("API Ï£ºÏÜå ÎòêÎäî ÌÜ†ÌÅ∞ ÏóÜÏùå");
      if (context.mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          const SnackBar(content: Text('ÏÑ§Ï†ï Ïò§Î•ò: API Ï£ºÏÜå ÎòêÎäî Ïù∏Ï¶ù ÌÜ†ÌÅ∞Ïù¥ ÏóÜÏäµÎãàÎã§')),
        );
      }
      return false;
    }

    try {
      final response = await dio.delete(
        '$apiUrl/cows/$cowId',
        options: Options(
          headers: {
            'Authorization': 'Bearer $token',
          },
        ),
      );

      return response.statusCode == 200 || response.statusCode == 204;
    } catch (e) {
      CowDetailPage._logger.severe("ÏÇ≠Ï†ú Ï§ë Ïò§Î•ò Î∞úÏÉù: $e");
      
      if (context.mounted) {
        ErrorUtils.handleError(
          context, 
          e, 
          customMessage: 'Ï†ñÏÜå ÏÇ≠Ï†ú Ï§ë Ïò§Î•òÍ∞Ä Î∞úÏÉùÌñàÏäµÎãàÎã§',
          defaultMessage: 'ÏÇ≠Ï†úÏóê Ïã§Ìå®ÌñàÏäµÎãàÎã§',
        );
      }
      return false;
    }
  }

